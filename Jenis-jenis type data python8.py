#Tipe Data Integer
foo = 1500
print(foo)
 
#Tipe Data Float
foo = 99.123
print(foo)
 
#Tipe Data Complex Number
foo = 4j
print(foo)
 
#Tipe Data Boolean
foo = True
print(foo)
 
#Tipe Data List
foo = ["satu","dua","tiga","satu"]
